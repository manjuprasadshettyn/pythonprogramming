# demo on if -elif- else

score = eval(input("Enter the marks "))

print ("your grade is ")

if score >= 90 and score <= 100 :
 print (" S")
elif score >= 75 and score < 90 :
 print (" A")
elif score >= 60 and score < 75 :
 print (" B")
elif score >= 40 and score < 60 :
 print (" C")
else :
 print (" F")

